                <!--市场部-->
                <p class="title_b">市场部</p>
                <p class="daoyu">
                　　市场部隶属行政管理中心，主要负责制定及实施品牌市场推广活动和公关活动，负责调研、研究市场及用户的
各类需求，负责对竞争品牌产品的性能、价格、促销手段等的收集、整理和分析，负责与校内外商家的商谈合作，
负责运营为师生免费提供电子商务服务的产品日新商城。
                </p>
