            	<p class="daoyu">
                 　　日新网行政管理中心是日新工作室部门之一，其前身为日新行政部。日新网行政管理中心的宗旨是打造交大最好的第二课堂社会实践平台。行政管理中心下设办公室、财务部、企划部、人力资源部、市场部，共5个部门。中心设有行政总监，部门设有办公室主任、财务部经理、企划部经理、人力资源部经理、市场部经理。行政管理中心对内主要负责网站内部的行政建设，运行日新部分网络产品及线下精品活动，做好网站的后勤服务、组织内部人员活动；对外则是与校内外各社团、学生会等其他学生组织合作，不定期与各高校兄弟网站之间开展交流、合作等活动，承担网站的部分外宣工作。
                </p>
                <br />
                <p id="1" class="title_b">办公室</p>
                <p class="daoyu">
                　　办公室是日新网行政管理中心部门之一，办公室主要负责协助全站进行网站的行政建设与校内外单位的走访交
流合作，负责学生维权工作，以及日新网的线下精品活动栏目，同时负责学校南区礼堂周末影院的日常管理及电影
放映宣传工作，此外，办公室负责对办公场所的使用情况进行登记归档，负责对来访人员的欢迎接待工作。
                </p>
                <p class="title_s">　办公室文员</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                    职位要求：<br />
    1、熟知办公室各项职能;<br />
    2、有一定的计算机基础，能熟练使用各种办公软件<br />
    3、具有较好的人际交往能力、沟通能力和应变能力;<br />
    4、做事认真，注重细节，责任心强，工作积极。<br />
    职位职责：<br />
    1、负责学校南区礼堂周末影院的日常管理;<br />
    2、协助办公室主任处理日常工作；<br />
    3、负责办公场所的分配和使用；
                    </p>
                </div>
                <div class="bottom_border" style="width:100%; height:27px; border-bottom:dashed 1px #7e7e7e;"></div>
                <br />
                <!--财政部-->
                 <p class="title_b">财务部</p>
                <p class="daoyu">
                　　财务部是行政管理中心所属部门之一。主要负责日新网的固定资产和资金收支的管理，负责整理各项费用并进
行归集和分类，负责固定资产报废、清理的账务登记，负责保管发票和单据及报销凭证，对活动预算进行初步审核
并上报。
                </p>
                <p class="title_s">　财务部业务员</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                    职位要求：<br />
    1、有较强的财务管理能力<br />
    2、做事公正，注重细节，责任心强，认真积极<br />
    3、具有较好的人际交往能力、沟通能力和应变能力;<br />
    职位职责:<br />
    1、明晰日新网各部门开销账目，整理规范、清楚明白；<br />
    2、负责日新网各项财产的登记、核对<br />
    3、及时将网站的收支，条目清晰的记录并归档<br />
    4、对活动预算进行初审，并及时上报
                    </p>
                </div>
                <div class="bottom_border" style="width:100%; height:27px; border-bottom:dashed 1px #7e7e7e;"></div>
                <br />
                <!--企划部-->
                <p class="title_b">企划部</p>
                <p class="daoyu">
                　　企划部隶属日新网行政管理中心，主要负责网站品牌推广、活动策划，建立和发展网站的网站文化、市场文化
和管理文化；负责网站活动项目进度的掌控，包括市场调研、信息搜集，组织、参与、指导策划及活动方案的制定
，完成网站推广项目的整体策划创意、设计；负责网站对外形象的建立与宣传，配合其他部门完成日常推广宣传工
作。
                </p>
                <p class="title_s">　企划部业务员</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                    职位要求：<br />
    1、有较强的活动策划及宣传推广能力<br />
    2、思维创新，注重细节，责任心强，积极主动。<br />
    3、擅长人际交往，有较强的沟通能力和应变能力;<br />
    职位职责：<br />
    1、负责网站品牌推广、活动策划；<br />
    2、建立和发展网站的网站文化、市场文化和管理文化<br />
    3、完成网站推广项目的整体策划创意、设计<br />
    4、负责网站对外形象的建立与宣传
                    </p>
                </div>
                <div class="bottom_border" style="width:100%; height:27px; border-bottom:dashed 1px #7e7e7e;"></div>
                <br />
                <!--人力资源部-->
                <p class="title_b">人力资源部</p>
                <p class="daoyu">
                　　人力资源部隶属行政管理中心，主要责任有：在各部门的协助下提出岗位设置调整意见；明确部门、岗位职责
及岗位任职资格；编制、修改和完善部门、岗位职责说明书；合理评价岗位价值。根据岗位需求状况和人力资源规
划，制定招聘计划，做好招聘前的准备、招聘实施和招聘后的手续完备等工作。组织建立绩效管理体系，做好考核
结果的汇总、审核和归档管理等工作。根据网站规划和新人发展需要，建立和完善新人培训体系；组织实施、指导
协调对新人进行的分类、分层次培训，努力提高新人素质。做好新人人事档案管理工作。定期汇总、编制人力资源
管理方面的相关统计报表和统计报告。
                </p>
                <p class="title_s">　人力资源部业务员（HR）</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
