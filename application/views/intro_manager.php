    1、熟练使用日常办公软件；<br />
    2、具备强烈责任感和工作主动性；<br />
    3、具备卓越的沟通及团队合作能力。<br />
    4、耐心细致，乐观开朗<br />
    职位职责：<br />
    1、编制、修改和完善部门、岗位职责说明书；<br />
    2、制定、策划、实施年度招聘计划；<br />
    3、建立绩效管理体系，做好各部门人员考核工作；<br />
    4、组织实施新人培训计划；<br />
    5、做好人事档案管理工作。
                    </p>
                </div>
                <div class="bottom_border" style="width:100%; height:27px; border-bottom:dashed 1px #7e7e7e;"></div>
